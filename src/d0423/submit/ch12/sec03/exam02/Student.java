package d0423.submit.ch12.sec03.exam02;

public class Student {
    private int no;
    private String name;

    public Student() {
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
